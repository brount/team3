package com.team.medical.vo;

public class FoodVO {
	private String food;
	private String foodData;
	private String foodMerit;
	private String foodDemerits;
	private String foodCaution;
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	public String getFoodData() {
		return foodData;
	}
	public void setFoodData(String foodData) {
		this.foodData = foodData;
	}
	public String getFoodMerit() {
		return foodMerit;
	}
	public void setFoodMerit(String foodMerit) {
		this.foodMerit = foodMerit;
	}
	public String getFoodDemerits() {
		return foodDemerits;
	}
	public void setFoodDemerits(String foodDemerits) {
		this.foodDemerits = foodDemerits;
	}
	public String getFoodCaution() {
		return foodCaution;
	}
	public void setFoodCaution(String foodCaution) {
		this.foodCaution = foodCaution;
	}

	
}

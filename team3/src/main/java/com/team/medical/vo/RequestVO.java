package com.team.medical.vo;

public class RequestVO {
	//정보수정요청목록
	private int dataCode;
	private int guestNo;
	private int doctorno;
	private int dataKind;
	private int dataNum;
	private int requestKind;
	private int processChek;
	private int hospital_no;
	public int getDataCode() {
		return dataCode;
	}
	public void setDataCode(int dataCode) {
		this.dataCode = dataCode;
	}
	public int getGuestNo() {
		return guestNo;
	}
	public void setGuestNo(int guestNo) {
		this.guestNo = guestNo;
	}
	public int getDoctorno() {
		return doctorno;
	}
	public void setDoctorno(int doctorno) {
		this.doctorno = doctorno;
	}
	public int getDataKind() {
		return dataKind;
	}
	public void setDataKind(int dataKind) {
		this.dataKind = dataKind;
	}
	public int getDataNum() {
		return dataNum;
	}
	public void setDataNum(int dataNum) {
		this.dataNum = dataNum;
	}
	public int getRequestKind() {
		return requestKind;
	}
	public void setRequestKind(int requestKind) {
		this.requestKind = requestKind;
	}
	public int getProcessChek() {
		return processChek;
	}
	public void setProcessChek(int processChek) {
		this.processChek = processChek;
	}
	public int getHospital_no() {
		return hospital_no;
	}
	public void setHospital_no(int hospital_no) {
		this.hospital_no = hospital_no;
	}
	
	

}
